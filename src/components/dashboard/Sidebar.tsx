interface SidebarProps {
  /** Simulated role — in a real app this comes from auth context */
  isProvider?: boolean;
}

export default function Sidebar({ isProvider = false }: SidebarProps) {
  const [mobileOpen, setMobileOpen] = useState(false);

  const items = navItems.filter((item) => !item.providerOnly || isProvider);

  const navContent = (
    <nav className="flex flex-col gap-1">
      {items.map((item) => {
        const isActive =
          typeof window !== "undefined" &&
          window.location.pathname === item.href;
        return (
          <a
            key={item.href}
            href={item.href}
            onClick={() => setMobileOpen(false)}
            className={`flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition-colors ${
              isActive
                ? "bg-primary-light text-primary"
                : "text-muted hover:bg-light-muted hover:text-foreground"
            }`}
          >
            {item.icon}
            {item.label}
          </a>
        );
      })}
    </nav>
  );

  return (
    <>
      {/* Mobile hamburger */}
      <button
        className="fixed bottom-4 right-4 z-50 flex h-12 w-12 items-center justify-center rounded-full bg-primary text-white shadow-lg lg:hidden"
        onClick={() => setMobileOpen(!mobileOpen)}
        aria-label="Toggle sidebar"
      >
        <svg className="h-5 w-5" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor">
          {mobileOpen ? (
            <path strokeLinecap="round" strokeLinejoin="round" d="M6 18L18 6M6 6l12 12" />
          ) : (
            <path strokeLinecap="round" strokeLinejoin="round" d="M3.75 6.75h16.5M3.75 12h16.5m-16.5 5.25h16.5" />
          )}
        </svg>
      </button>

      {/* Mobile overlay */}
      {mobileOpen && (
        <div
          className="fixed inset-0 z-40 bg-black/30 lg:hidden"
          onClick={() => setMobileOpen(false)}
        />
      )}

      {/* Sidebar */}
      <aside
        className={`fixed top-16 left-0 z-40 h-[calc(100vh-4rem)] w-64 border-r border-border bg-white transition-transform duration-200 lg:sticky lg:translate-x-0 ${
          mobileOpen ? "translate-x-0" : "-translate-x-full"
        }`}
      >
        <div className="flex h-full flex-col p-4">
          <div className="mb-6 px-3">
            <p className="text-xs font-semibold uppercase tracking-wider text-muted">
              Dashboard
            </p>
          </div>
          {navContent}
        </div>
      </aside>
    </>
  );
}
